def write_csv(filename, data):
    """写入 CSV 文件"""
    filepath = os.path.join(OUTPUT_DIR, filename)
    with open(filepath, 'w', newline='', encoding='utf-8') as f:
        writer = csv.writer(f)
        writer.writerow(['university_name', 'year', 'subject_type', 'major_group',
                         'subject_requirement', 'min_score', 'min_rank'])
        for row in data:
            writer.writerow(row)

    uni_count = len(set(r[0] for r in data))
    group_count = len(data)
    print(f'  {filename}: {group_count}条记录, 覆盖{uni_count}所院校')


