def write_csv(filename, data, year, subject_type):
    """写入 CSV 文件，并验证数据"""
    filepath = os.path.join(OUTPUT_DIR, filename)
    with open(filepath, 'w', newline='', encoding='utf-8') as f:
        writer = csv.writer(f)
        writer.writerow(['year', 'subject_type', 'score', 'rank'])
        for score, rank in data:
            writer.writerow([year, subject_type, score, rank])

    # 验证
    scores = [r[0] for r in data]
    ranks = [r[1] for r in data]

    errors = []
    # 1. 分数单调递减
    for i in range(len(scores)-1):
        if scores[i] <= scores[i+1]:
            errors.append(f'分数不递减: {scores[i]} -> {scores[i+1]}')
    # 2. 位次单调递增（分数越低→位次越大）
    for i in range(len(ranks)-1):
        if ranks[i] >= ranks[i+1]:
            errors.append(f'位次不递增: score={scores[i]} rank={ranks[i]} -> score={scores[i+1]} rank={ranks[i+1]}')
    # 3. 无重复分数
    if len(scores) != len(set(scores)):
        errors.append('存在重复分数')

    status = '✅ 验证通过' if not errors else f'❌ {len(errors)}个错误'
    completeness = '逐分完整' if len(data) > 50 else f'部分({len(data)}个采样点)'
    print(f'  {filename}: {len(data)}行, {completeness}, {status}')
    if errors:
        for e in errors[:3]:
            print(f'    ⚠️ {e}')

    return filepath


